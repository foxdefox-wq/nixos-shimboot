#!/bin/bash
# apply_shimboot_fix.sh v3 -- in-place fix for the NixOS branch of shimboot.
#
# WHAT THIS SHIPS:
#   build_rootfs.sh         -- nix-build instead of nixos-install, plus
#                              activation prep (/sbin/init, /bin/sh symlinks)
#   build_complete.sh       -- accept Ubuntu hosts with nix in non-default PATH
#   build.sh                -- pass partition name to mkfs for ext4 label
#   image_utils.sh          -- mkfs.ext4 -L <name> so by-label/nixos works
#   patch_rootfs.sh         -- mkdir -p /lib before copying ChromeOS modules
#   rootfs/opt/setup_rootfs_nixos.sh
#                           -- minimal config close to ading2210's defaults:
#                              lightdm + xfce, networkmanager, zram, expand_rootfs.
#                              Adds boot.loader.initScript.enable = true (the
#                              NixOS knob for shimboot-style boots). Includes
#                              a commented-out systemd patch for old shim kernels
#                              (uncomment if boot hangs).
#
# WHAT THIS DOES NOT SHIP (deliberately):
#   - Fish, Hyprland, headless mode, board manifests, flake.nix, custom
#     bootloader -- everything PopCat's fork adds. The goal is to stay as
#     close to ading2210/shimboot upstream as possible.
#
# USAGE:
#   cd ~/shimboot-nixos
#   bash apply_shimboot_fix.sh
#   # then:
#   sudo rm -rf data/rootfs_dedede
#   sudo ./build_complete.sh dedede distro=nixos

set -e

if [ ! -f "build_complete.sh" ] || [ ! -d "rootfs/opt" ]; then
  echo "ERROR: run from inside your shimboot-nixos directory."
  echo "       cd ~/shimboot-nixos && bash $(basename "$0")"
  exit 1
fi

echo ">> backing up old files to *.bak"
for f in build_rootfs.sh build_complete.sh build.sh image_utils.sh \
         patch_rootfs.sh rootfs/opt/setup_rootfs_nixos.sh; do
  [ -f "$f" ] && cp -f "$f" "$f.bak"
done

write_b64() {
  local target="$1" marker="$2"
  echo ">> writing $target"
  sed -n "/^# BEGIN:$marker$/,/^# END:$marker$/p" "$0" \
    | sed "1d;\$d" | base64 -d > "$target"
}

write_b64 build_rootfs.sh                  BUILD_ROOTFS
write_b64 rootfs/opt/setup_rootfs_nixos.sh SETUP_NIXOS
write_b64 build_complete.sh                BUILD_COMPLETE
write_b64 build.sh                         BUILD
write_b64 image_utils.sh                   IMAGE_UTILS
write_b64 patch_rootfs.sh                  PATCH_ROOTFS

chmod +x build_rootfs.sh build_complete.sh build.sh image_utils.sh \
         patch_rootfs.sh rootfs/opt/setup_rootfs_nixos.sh

echo ""
echo ">> actual checksums:"
md5sum build_rootfs.sh rootfs/opt/setup_rootfs_nixos.sh build_complete.sh \
       build.sh image_utils.sh patch_rootfs.sh

cat <<'EXPECT'

>> expected checksums:
cb8c506c2c36bc2de54fb60f1e67107f  build_rootfs.sh
6842a1a6d2d61039100c154356c4ab73  rootfs/opt/setup_rootfs_nixos.sh
02d16fc78704608536e1639d0fe437da  build_complete.sh
9a78604507382ec1d22bf460b34ae20a  build.sh
06320b1ecddb9f0e3b5a5419e5bba3fb  image_utils.sh
600e9d6fd68651c96042896d1a90d6cf  patch_rootfs.sh

EXPECT

echo ">> sanity checks (all should be 1):"
echo -n "  /sbin/init symlink in build_rootfs.sh: "
grep -c 'sbin/init' build_rootfs.sh || true
echo -n "  initScript.enable in setup_rootfs_nixos.sh: "
grep -c 'initScript.enable = true' rootfs/opt/setup_rootfs_nixos.sh || true
echo -n "  mkdir -p .../lib in patch_rootfs.sh: "
grep -c 'mkdir -p "${target_rootfs}/lib"' patch_rootfs.sh || true
echo -n "  -L \"\$rootfs_label\" in image_utils.sh: "
grep -c 'rootfs_label_arg' image_utils.sh || true

cat <<'NEXT'

>> NEXT STEPS

Your previous rootfs at data/rootfs_dedede is incomplete (no /sbin/init).
Best to rebuild from scratch -- but you can skip the slow shim/reco
download by reusing the already-downloaded *.bin files:

  sudo rm -rf data/rootfs_dedede
  sudo ./build_complete.sh dedede distro=nixos

If you've used --enable-root or want a custom desktop:
  sudo ./build_complete.sh dedede distro=nixos desktop=gnome

If boot hangs at an early systemd error (mount_setattr / open_tree),
edit data/rootfs_dedede/etc/nixos/configuration.nix on the USB and
uncomment the 'systemd.package = pkgs.systemd.overrideAttrs ...' block,
then run `nixos-rebuild switch` on the booted device.

NEXT
exit 0


