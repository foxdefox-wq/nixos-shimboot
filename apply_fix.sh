#!/bin/bash
# apply_shimboot_fix.sh v5 — adds boot.postBootCommands so the missing API
# filesystems (/dev/pts, /dev/shm, /run, etc.) get mounted before systemd
# is exec'd. v5 reverts v4's `boot.initrd.enable = false` because some
# nixos-24.05 modules reference system.build.initialRamdisk unconditionally
# and disabling the initrd breaks eval. The initrd just gets built and
# never used -- harmless ~30MB.
#
# USAGE:
#   cd ~/shimboot-nixos
#   bash apply_shimboot_fix.sh
#   sudo rm -rf data/rootfs_dedede
#   sudo ./build_complete.sh dedede distro=nixos

set -e

if [ ! -f "build_complete.sh" ] || [ ! -d "rootfs/opt" ]; then
  echo "ERROR: run from inside your shimboot-nixos directory."
  exit 1
fi

echo ">> backing up old files to *.bak"
for f in build_rootfs.sh build_complete.sh build.sh image_utils.sh \
         patch_rootfs.sh rootfs/opt/setup_rootfs_nixos.sh; do
  [ -f "$f" ] && cp -f "$f" "$f.bak"
done

write_b64() {
  local target="$1" marker="$2"
  echo ">> writing $target"
  sed -n "/^# BEGIN:$marker$/,/^# END:$marker$/p" "$0" \
    | sed "1d;\$d" | base64 -d > "$target"
}

write_b64 build_rootfs.sh                  BUILD_ROOTFS
write_b64 rootfs/opt/setup_rootfs_nixos.sh SETUP_NIXOS
write_b64 build_complete.sh                BUILD_COMPLETE
write_b64 build.sh                         BUILD
write_b64 image_utils.sh                   IMAGE_UTILS
write_b64 patch_rootfs.sh                  PATCH_ROOTFS

chmod +x build_rootfs.sh build_complete.sh build.sh image_utils.sh \
         patch_rootfs.sh rootfs/opt/setup_rootfs_nixos.sh

echo ""
echo ">> actual checksums:"
md5sum build_rootfs.sh rootfs/opt/setup_rootfs_nixos.sh build_complete.sh \
       build.sh image_utils.sh patch_rootfs.sh

cat <<'EXPECT'

>> expected checksums:
cb8c506c2c36bc2de54fb60f1e67107f  build_rootfs.sh
27f64e336818226dc1574cda0d65b238  rootfs/opt/setup_rootfs_nixos.sh
02d16fc78704608536e1639d0fe437da  build_complete.sh
9a78604507382ec1d22bf460b34ae20a  build.sh
06320b1ecddb9f0e3b5a5419e5bba3fb  image_utils.sh
600e9d6fd68651c96042896d1a90d6cf  patch_rootfs.sh

EXPECT

echo ">> sanity checks:"
echo -n "  postBootCommands present (expect >=1): "
grep -c "boot.postBootCommands" rootfs/opt/setup_rootfs_nixos.sh || true
echo -n "  initrd.enable = false absent (expect 0): "
grep -c "boot.initrd.enable = false" rootfs/opt/setup_rootfs_nixos.sh || true
echo -n "  systemd.package patch absent (expect 0): "
grep -c "systemd.package = " rootfs/opt/setup_rootfs_nixos.sh || true
echo -n "  initScript.enable = true (expect 1): "
grep -c "initScript.enable = true" rootfs/opt/setup_rootfs_nixos.sh || true

cat <<'NEXT'

>> NEXT STEPS

Existing rootfs is fine, just need to rebuild the NixOS closure with the
fixed config. Fastest path -- skip downloads, rebuild closure only:

  cd ~/shimboot-nixos
  sudo /nix/var/nix/profiles/default/bin/nix-build '<nixpkgs/nixos>' \
    -A config.system.build.toplevel \
    -I nixos-config=$PWD/data/rootfs_dedede/etc/nixos/configuration.nix \
    --out-link /tmp/nixos-system

Then re-copy the closure and re-link the system profile:

  SYS_PATH="$(readlink -f /tmp/nixos-system)"
  sudo /nix/var/nix/profiles/default/bin/nix \
    --extra-experimental-features 'nix-command' copy \
    --no-check-sigs \
    --to "local?root=$PWD/data/rootfs_dedede" "$SYS_PATH"
  sudo ln -sfT "$SYS_PATH" \
    "$PWD/data/rootfs_dedede/nix/var/nix/profiles/system"
  sudo ln -sfT /nix/var/nix/profiles/system/init \
    "$PWD/data/rootfs_dedede/sbin/init"
  sudo ln -sfT /nix/var/nix/profiles/system/init \
    "$PWD/data/rootfs_dedede/init"

Then build the image:

  sudo ./patch_rootfs.sh data/shim_dedede.bin data/reco_dedede.bin data/rootfs_dedede
  sudo ./build.sh data/shimboot_dedede.bin data/shim_dedede.bin data/rootfs_dedede name=nixos

Or just nuke and rerun (slower but simpler):
  sudo rm -rf data/rootfs_dedede
  sudo ./build_complete.sh dedede distro=nixos

NEXT
exit 0


