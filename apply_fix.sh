#!/bin/bash
# apply_shimboot_fix.sh -- in-place fix for the NixOS branch of shimboot.
# Run from inside your shimboot-nixos directory:
#   cd ~/shimboot-nixos && bash apply_shimboot_fix.sh
#
# Patches 6 files, backs up the originals to *.bak, verifies checksums.
# No git, no network, no copies from elsewhere -- payloads embedded as base64.

set -e

if [ ! -f "build_complete.sh" ] || [ ! -d "rootfs/opt" ]; then
  echo "ERROR: run this from inside your shimboot-nixos directory."
  echo "       cd ~/shimboot-nixos && bash $(basename "$0")"
  exit 1
fi

echo ">> backing up old files to *.bak"
for f in build_rootfs.sh build_complete.sh build.sh image_utils.sh \
         patch_rootfs.sh rootfs/opt/setup_rootfs_nixos.sh; do
  [ -f "$f" ] && cp -f "$f" "$f.bak"
done

write_b64() {
  local target="$1"
  local marker="$2"
  echo ">> writing $target"
  # extract everything between BEGIN:marker and END:marker from this script
  sed -n "/^# BEGIN:$marker$/,/^# END:$marker$/p" "$0" \
    | sed "1d;\$d" \
    | base64 -d > "$target"
}

write_b64 build_rootfs.sh                  BUILD_ROOTFS
write_b64 rootfs/opt/setup_rootfs_nixos.sh SETUP_NIXOS
write_b64 build_complete.sh                BUILD_COMPLETE
write_b64 build.sh                         BUILD
write_b64 image_utils.sh                   IMAGE_UTILS
write_b64 patch_rootfs.sh                  PATCH_ROOTFS

chmod +x build_rootfs.sh build_complete.sh build.sh image_utils.sh \
         patch_rootfs.sh rootfs/opt/setup_rootfs_nixos.sh

echo ""
echo ">> actual checksums:"
md5sum build_rootfs.sh rootfs/opt/setup_rootfs_nixos.sh build_complete.sh \
       build.sh image_utils.sh patch_rootfs.sh

cat <<'EXPECT'

>> expected checksums:
d30bddfdf244a79be145a1af5b599ab4  build_rootfs.sh
7c088e02951cb6fd625246d3ffbdd828  rootfs/opt/setup_rootfs_nixos.sh
02d16fc78704608536e1639d0fe437da  build_complete.sh
9a78604507382ec1d22bf460b34ae20a  build.sh
06320b1ecddb9f0e3b5a5419e5bba3fb  image_utils.sh
600e9d6fd68651c96042896d1a90d6cf  patch_rootfs.sh

EXPECT

echo ">> sanity checks (all should be 1):"
echo -n "  nix-build appears in build_rootfs.sh: "
grep -c "nix-build" build_rootfs.sh || true
echo -n "  environment.systemPackages in setup_rootfs_nixos.sh: "
grep -c "environment.systemPackages" rootfs/opt/setup_rootfs_nixos.sh || true
echo -n "  mkdir -p .../lib in patch_rootfs.sh: "
grep -c 'mkdir -p "${target_rootfs}/lib"' patch_rootfs.sh || true

echo ""
echo "All files patched. To rerun the build (rootfs is already built, you can"
echo "skip straight to patch + image steps to save the long nix-build):"
echo ""
echo "  sudo ./patch_rootfs.sh data/shim_dedede.bin data/reco_dedede.bin data/rootfs_dedede"
echo "  sudo ./build.sh data/shimboot_dedede.bin data/shim_dedede.bin data/rootfs_dedede name=nixos"
echo ""
echo "Or from scratch:"
echo "  sudo rm -rf data/rootfs_dedede"
echo "  sudo ./build_complete.sh dedede distro=nixos"

exit 0


